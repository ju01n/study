/*
문제) 변수 hour에 0~23 사이의 시간을 나타내는 숫자를 할당후
        아래 조건에 따른 메세지를 출력하시오.

        조건1. 19<=hour<21 이면 "도시락 30% 할인" 출력 
        조건2. 9시 또는 15시 정각이면 "도시락 하나 사면 하나 공짜~!"
        조건3. 그 외의 시간은 "도시락 사세요~!" 라고 출력
*/

const hour = 5;

if(hour >= 19 && hour < 21){
  console.log('도시락 30% 할인');
}else if(hour === 9 || hour === 15){
  console.log('도시락 하나 사면 하나 공짜~');
}else{
  console.log('도시락 사세요~!');
}